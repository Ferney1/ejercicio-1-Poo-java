public class Intrumentos {

    String tocar;
    String sonidos;

    public Intrumentos() {
    }

    public Intrumentos(String tocar, String sonidos) {
        this.tocar = tocar;
        this.sonidos = sonidos;
    }

    public String getTocar() {
        return tocar;
    }

    public void setTocar(String tocar) {
        this.tocar = tocar;
    }

    public String getSonidos() {
        return sonidos;
    }

    public void setSonidos(String sonidos) {
        this.sonidos = sonidos;
    }
}
